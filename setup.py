# cf http://bugs.python.org/issue26689
def has_flag(compiler, flag):
    """Return a boolean indicating whether a flag name is supported on
    the specified compiler.
    """
    import tempfile

    with tempfile.TemporaryDirectory() as tmpdir:
        fname = join(tmpdir, "flagcheck.cpp")
        with open(fname, "w") as f:
            # Don't trigger -Wunused-parameter.
            f.write("int main (int, char **) { return 0; }")

        try:
            compiler.compile([fname], output_dir=tmpdir, extra_postargs=[flag])
        except setuptools.distutils.errors.CompileError:
            return False
    return True


def cpp_flag(compiler, pfx, sep="="):
    """Return the -std=c++[17/20] compiler flag.
    The newer version is prefered when it is available).
    """

    flags = [
        f"{pfx}std{sep}c++20",
        f"{pfx}std{sep}c++17",
    ]

    for flag in flags:
        if has_flag(compiler, flag):
            return flag

    raise RuntimeError("Unsupported compiler -- at least C++17 support is needed!")


class BuildExt(build_ext):
    """A custom build extension for adding compiler-specific options."""

    c_opts = {"msvc": ["/EHsc", "/DNOMINMAX"], "unix": []}

    if sys.platform == "darwin":
        c_opts["unix"] += ["-stdlib=libc++", "-mmacosx-version-min=10.14"]

    def build_extensions(self):
        ct = self.compiler.compiler_type
        opts = self.c_opts.get(ct, [])
        if ct == "unix":
            # TODO: this feels like a hack
            if not os.environ.get("RPY_DEBUG"):
                opts.append("-s")  # strip
                opts.append("-g0")  # remove debug symbols
            else:
                opts.append("-O0")
            opts.append(cpp_flag(self.compiler, "-"))
            if has_flag(self.compiler, "-fvisibility=hidden"):
                opts.append("-fvisibility=hidden")
            if sys.platform != "darwin":
                opts.append("-D_GNU_SOURCE")
        elif ct == "msvc":
            opts.append(cpp_flag(self.compiler, "/", ":"))
            opts.append("/Zc:__cplusplus")
        for ext in self.extensions:
            ext.define_macros.append(("PYBIND11_USE_SMART_HOLDER_AS_DEFAULT", "1"))
            ext.extra_compile_args = opts
        build_ext.build_extensions(self)


def recursive_glob(d):
    for root, _, filenames in os.walk(d):
        for fname in fnmatch.filter(filenames, "*.cpp"):
            yield join(root, fname)


def get_cscore_sources(d):
    l = list(glob.glob(d + "/cpp/*.cpp"))
    if sys.platform == "win32":
        l.extend(glob.glob(d + "/windows/*.cpp"))
    elif sys.platform == "darwin":
        l.extend(glob.glob(d + "/osx/*.cpp"))
    else:
        l.extend(glob.glob(d + "/linux/*.cpp"))
    return l


def get_wpiutil_sources(d):
    if external_wpiutil:
        return []

    jni = re.compile(r"[\\/]jni[\\/]")
    l = [f for f in recursive_glob(d + "/cpp") if not jni.search(f)]
    if sys.platform == "win32":
        l.extend(glob.glob(d + "/windows/*.cpp"))
    else:
        l.extend(glob.glob(d + "/unix/*.cpp"))
        if sys.platform == "darwin":
            l.extend(glob.glob(d + "/macOS/*.cpp"))
        else:
            l.extend(glob.glob(d + "/linux/*.cpp"))
    return l


